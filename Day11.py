# Define constants for calculation
seconds_per_minute = 60
minutes_per_hour = 60
hours_per_day = 24
days_per_year = 365

# Calculate seconds in a year
seconds_per_year = seconds_per_minute * minutes_per_hour * hours_per_day * days_per_year

# Display the result
print("Seconds in a year:", seconds_per_year)
